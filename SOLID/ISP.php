/*
Interface Segregations Principle
    Clientes não devem ser forçados a depender de métodos
    que eles não usam. 
    Ou seja, uma classe não deve ser forçada a implementar
    uma interface com métodos que não faz sentido pra
    ela

    Imagine uma lista de exercicios dada a dois robos
    porem, um dos exercicios é mover as antenas, mas
    um dos nossos robos nao tem antena. Então nao faz
    sentido ele fazer esse exercicio.

    Por isso, devemos separar os exercicios de acordo
    com cada tipo de robo 

    Imagine uma interface que vai passar metodos pra
    uma classe cachorro e uma classe passaro. Essa
    interface nao pode passar um método voar, pois
    vai forçar a classe cachorro a aplicar esse método

    Adapte interfaces para necessidades
    individuais dos clientes


    
*/






